namespace General;

/// <summary>
/// Класс-оболочка (капсула) для хранения строкового значения.
/// Может использоваться для передачи строковой переменной по ссылке или в контекстах,
/// где требуется изменяемый (mutable) объект для хранения строки.
/// </summary>
public class StringCapsule()
{
    // Примечание: В C# строка (string) является ссылочным типом, но неизменяемым (immutable). 
    // Создание StringCapsule позволяет обернуть строку в изменяемый класс, 
    // чтобы передавать ссылку на сам объект-капсулу, а не только на строку.

    /// <summary>
    /// Фактическое строковое значение, хранимое в капсуле.
    /// Инициализировано пустой строкой.
    /// </summary>
    public string Value = string.Empty;
}
